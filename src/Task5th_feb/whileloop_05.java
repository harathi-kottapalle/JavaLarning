package Task5th_feb;

public class whileloop_05 {
    public static void main(String[] args) {
        //5️⃣ Print the multiplication table of 7
        int num =7;
        int i=1;
        while (i<=10) {

            System.out.println(num + "x" + i + "=" + (num * i));
            i++;
        }
    }
}

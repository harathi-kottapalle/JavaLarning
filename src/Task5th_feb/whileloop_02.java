package Task5th_feb;

public class whileloop_02 {
    public static void main(String[] args) {
        //2️⃣ Print even numbers from 1 to 20
        int i=1;
        while (i<=20){
            if (i%2==0) {
                System.out.println(i);
            }
                i++;

        }
    }
}

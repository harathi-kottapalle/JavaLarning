package Task5th_feb;

public class whileloop_04 {
    public static void main(String[] args) {
        //4️⃣ Calculate sum of first 10 natural numbers
        int sum = 0;
        int i = 1;
        while (i <= 10) {
            sum += i;
            i++;
        }

        System.out.println("sum of first 10 natural number is :" + sum);




    }
}
